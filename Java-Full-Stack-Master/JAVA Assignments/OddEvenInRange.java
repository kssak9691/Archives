public class OddEvenInRange{
	public static void main(String[] args) {
		int range=85,a=1,b=2;
		System.out.println("Odd numbers are:");
		while(a<=range){
			System.out.println(a);
			a+=2;
		}
		System.out.println("Even Numbers:");
		while(b<=range){
			System.out.println(b);
			b+=2;
		}
	}
}